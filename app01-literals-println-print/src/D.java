class D 
{
	public static void main(String[] args) 
	{
		System.out.println('A');  //Char literal
		System.out.println('K');
		System.out.println('S');
		System.out.println('C');
		System.out.println('B');
		System.out.println('1');
		System.out.println('$');
	}
}
//Only one character Under single quotes is a char literal. It may be any digit, any
//alphabet or special character such as !,@,$,% or even single space.
//Note: space is also considered as a special character