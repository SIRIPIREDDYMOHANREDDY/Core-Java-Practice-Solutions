class G 
{
	public static void main(String[] args) 
	{
		int i = 5;
        System.out.println(i--);   // post decrement
		System.out.println(i);
		System.out.println("-----------");
		int j = 5;
		System.out.println(--j);   // pre decrement
		System.out.println(j);
		System.out.println("-----------");
	}
}
