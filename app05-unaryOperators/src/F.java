class F 
{
	public static void main(String[] args) 
	{
		int i = 0;
		System.out.println(i++);   // post increment
		System.out.println(i);
		System.out.println("-----------");
		int j = 0;
		System.out.println(++j);   // pre increment
		System.out.println(j);
		System.out.println("-----------");
	}
}
