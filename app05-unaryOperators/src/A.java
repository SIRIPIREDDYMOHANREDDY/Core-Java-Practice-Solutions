//Unary Operators

class A 
{
	public static void main(String[] args) 
	{
		int i = 1; 
		System.out.println(i);
		i++;               // post way of using increment unary operator
		System.out.println(i);
	}
}


/*
Unary operators:
These are operators that bring one unit effect in the variable.
There are two unary operators available
1. Increment operator ++ increase variable value by one unit.
2. Decrement operator - - decrease variable value by one unit.
Both unary operators can be used in two ways
1. post way of using : If we use the operator after the variable.
2. pre way of using: if we use the operator before the variable.

*/